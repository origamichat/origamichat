export function SignInButtons() {
  const handleSignIn = async () => {
    await signIn.social(
      {
        provider: "google",
        callbackURL: `${process.env.NEXT_PUBLIC_BASE_URL}/auth`,
      },
      {
        credentials: "include",
      }
    );
  };

  return (
    <div className="flex flex-col gap-2">
      <Button onClick={handleSignIn}>
        <GoogleIcon />
        Sign in with google
      </Button>
    </div>
  );
}
