export const Icon: React.FC<IconProps> = ({
	name,
	variant = "default",
	className,
	filledOnHover,
}) => {
	const defaultContent = getIconContent(name, "default");
	const filledContent = getIconContent(name, "filled");
	const currentContent = getIconContent(name, variant);
	if (!currentContent) {
		return null;
	}

	const renderWithClass = (content: ReactNode, extraClassName?: string) => (
		<g className={extraClassName}>{content}</g>
	);

	// If filledOnHover is true, render both variants with conditional visibility
	if (filledOnHover && defaultContent && filledContent) {
		return (
			<svg
				className={cn("size-4", className)}
				fill="none"
				height="24"
				viewBox="0 0 24 24"
				width="24"
				xmlns="http://www.w3.org/2000/svg"
			>
				<title>{name}</title>
				{renderWithClass(
					defaultContent,
					"transition-opacity duration-200 group-hover/btn:opacity-0"
				)}
				{renderWithClass(
					filledContent,
					"opacity-0 transition-opacity duration-200 group-hover/btn:opacity-100"
				)}
			</svg>
		);
	}

	// Standard rendering for non-hover variants
	return (
		<svg
			className={cn("size-4", className)}
			fill="none"
			height="24"
			viewBox="0 0 24 24"
			width="24"
			xmlns="http://www.w3.org/2000/svg"
		>
			<title>{name}</title>
			{currentContent}
		</svg>
	);
};

